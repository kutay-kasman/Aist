from torch.utils.data import Dataset
import torch
import os
import numpy as np
import torch.nn as nn


# üé≠ Genre label mapping
genre_to_idx = {
    'BR': 0, 'HO': 1, 'JB': 2, 'JS': 3, 'KR': 4,
    'LH': 5, 'LO': 6, 'MH': 7, 'PO': 8, 'WA': 9
}

# üì¶ FusedPoseDataset: supports 60√ó118 input (keypoints + angles + deltas + accel)
class FusedPoseDataset(Dataset):
    def __init__(self, folder_path):
        self.samples = []
        for file in os.listdir(folder_path):
            if not file.endswith(".npz"):
                continue

            path = os.path.join(folder_path, file)
            try:
                data = np.load(path)
                seq = data["seq"]  # shape: (60, 118)
                label_str = str(data["label"])  # e.g. 'BR'
                label = genre_to_idx.get(label_str, -1)

                # ‚úÖ Safety check
                if label == -1 or seq.shape != (60, 118):
                    print(f"‚ö†Ô∏è Skipping {file}: shape={seq.shape}, label={label_str}")
                    continue

                self.samples.append((
                    torch.tensor(seq, dtype=torch.float32),      # (60, 118)
                    torch.tensor(label, dtype=torch.long)        # class index
                ))
            except Exception as e:
                print(f"‚ùå Error loading {file}: {e}")
                continue

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        return self.samples[idx]

"""# Define LSTM Model"""

import torch.nn as nn

class DanceLSTM(nn.Module):
    def __init__(self, input_size=118, hidden_size=128, num_layers=2, num_classes=10):
        super().__init__()
        self.lstm = nn.LSTM(
            input_size, hidden_size, num_layers,
            batch_first=True, bidirectional=True, dropout=0.3  # ‚¨ÖÔ∏è Dropout added
        )
        self.dropout = nn.Dropout(0.3)  # ‚¨ÖÔ∏è Extra safety
        self.fc = nn.Linear(hidden_size * 2, num_classes)

    def forward(self, x):  # x: (B, 60, 118)
        out, _ = self.lstm(x)
        out = self.dropout(out[:, -1, :])  # ‚¨ÖÔ∏è Apply dropout at final time step
        return self.fc(out)

"""# Train Model"""

from torch.utils.data import DataLoader
import torch
from tqdm import tqdm
from torch.optim import Adam
import os

# ‚úÖ Use the updated 60-frame fused input folder
DATA_DIR = "/content/fused_chunks_60"  # ‚Üê changed from _120

# ‚úÖ Load fused datasets (60, 118)
train_set = FusedPoseDataset(os.path.join(DATA_DIR, "train"))
val_set   = FusedPoseDataset(os.path.join(DATA_DIR, "val"))

# ‚úÖ DataLoaders
train_loader = DataLoader(train_set, batch_size=16, shuffle=True)
val_loader   = DataLoader(val_set, batch_size=16)

# ‚úÖ Device
DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")

# ‚úÖ Model for (60, 118) input
model = DanceLSTM(input_size=118).to(DEVICE)  # no change here

# ‚úÖ Optimizer and Loss
optimizer = Adam(model.parameters(), lr=0.001)
criterion = torch.nn.CrossEntropyLoss()

# ‚úÖ Track best model
best_val_acc = 0.0

# ‚úÖ Training loop
EPOCHS = 50
for epoch in range(1, EPOCHS + 1):
    model.train()
    total_loss = 0

    for X, y in tqdm(train_loader, desc=f"Epoch {epoch}"):
        X, y = X.to(DEVICE), y.to(DEVICE)
        optimizer.zero_grad()
        out = model(X)  # (B, 10)
        loss = criterion(out, y)
        loss.backward()
        optimizer.step()
        total_loss += loss.item()

    # ‚úÖ Validation
    model.eval()
    correct = total = 0
    with torch.no_grad():
        for X, y in val_loader:
            X, y = X.to(DEVICE), y.to(DEVICE)
            preds = model(X).argmax(dim=1)
            correct += (preds == y).sum().item()
            total += y.size(0)

    val_acc = correct / total * 100
    print(f"‚úÖ Epoch {epoch}: Train Loss = {total_loss:.4f}, Val Accuracy = {val_acc:.2f}%")

    # üíæ Save best model
    if val_acc > best_val_acc:
        best_val_acc = val_acc
        torch.save(model.state_dict(), "best_dance_lstm_60.pth")  # ‚Üê renamed to avoid overwrite
        print(f"üì¶ Saved new best model (val acc = {val_acc:.2f}%)")

MODEL_PATH = "/content/best_dance_lstm_60.pth"
