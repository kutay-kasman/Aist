import os
import numpy as np

def clean_all_bad_files(data_dir, splits, min_thresh=-50, max_thresh=50, expected_shape=(60, 118)):
    deleted = 0
    issues = 0

    for split in splits:
        split_path = os.path.join(data_dir, split)
        print(f"\nüîç Checking split: {split}")

        for fname in sorted(os.listdir(split_path)):
            if not fname.endswith(".npz"):
                continue
            path = os.path.join(split_path, fname)

            try:
                data = np.load(path)
                seq = data["seq"]

                has_issue = False

                # Check shape
                if seq.shape != expected_shape:
                    print(f"‚ùå Wrong shape in {fname}: got {seq.shape}, expected {expected_shape}")
                    has_issue = True

                # Check NaNs and Infs
                if np.isnan(seq).any():
                    print(f"‚ùå NaNs in {fname}")
                    has_issue = True
                if np.isinf(seq).any():
                    print(f"‚ùå Infs in {fname}")
                    has_issue = True

                # Check min/max range
                min_val = np.min(seq)
                max_val = np.max(seq)
                if min_val < min_thresh or max_val > max_thresh:
                    print(f"‚ö†Ô∏è Extreme values in {fname}: min={min_val:.2f}, max={max_val:.2f}")
                    has_issue = True

                # Delete if anything is wrong
                if has_issue:
                    os.remove(path)
                    print(f"üóëÔ∏è Deleted {fname}")
                    deleted += 1
                    issues += 1

            except Exception as e:
                print(f"‚ùå Error loading {fname}: {e}")
                try:
                    os.remove(path)
                    print(f"üóëÔ∏è Deleted corrupted file: {fname}")
                    deleted += 1
                    issues += 1
                except PermissionError:
                    print(f"‚ö†Ô∏è Could not delete {fname} (locked or permission issue)")

    if deleted == 0:
        print("\n‚úÖ All .npz files look clean!")
    else:
        print(f"\nüßπ Deleted {deleted} bad files with issues.")

